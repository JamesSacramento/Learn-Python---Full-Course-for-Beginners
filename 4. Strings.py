#String
print("James Sacramento")
#String with new line
print("James \nSacramento")
#String with escaped characters
print("\"James \nSacramento\"")
#string in a variable
phrase = "James Sacramento is cool"
print(phrase)
#concatenation
print(phrase + "cool")
#phrase.lower
print(phrase.lower())
#phrase.upper
print(phrase.upper())
#checking upper or lower
print(phrase.isupper())
print(phrase.islower())
#length
print(len(phrase))
